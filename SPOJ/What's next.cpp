#include <iostream>

using namespace std;

int main ()
{
	float num[4];
   	while(true)
   	{	
   		for(int i=0;i<3;i++)
   		{
   			cin>>num[i];
   		}
   		if((num[0]==0)&&(num[1]==0)&&(num[2]==0))
   		{
   			break;
   		}
		if(2*num[1]==(num[0]+num[2]))
		{
			float cd=num[1]-num[0];
			num[3]=num[2]+cd;
			cout<<"AP "<<num[3]<<endl;
		}
		else
		{
			float cr=num[1]/num[0];
			num[3]=num[2]*cr;
			cout<<"GP "<<num[3]<<endl;
		}
	}

return 0;
}